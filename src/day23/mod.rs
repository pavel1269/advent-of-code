pub fn get_solution_part1() -> i64 {
    // A = 0, B = 0, C = 0, D = 0
    // #############
    // #...........#
    // ###D#A#A#D###
    //   #C#C#B#B#
    //   #########

    // A = 0, B = 0, C = 0, D = 2*
    // #############
    // #.........D.#
    // ###D#A#A#.###
    //   #C#C#B#B#
    //   #########

    // A = 5 + 6*, B = 0, C = 0, D = 2
    // #############
    // #AA.......D.#
    // ###D#.#.#.###
    //   #C#C#B#B#
    //   #########

    // A = 11, B = 3*, C = 6*, D = 2
    // #############
    // #AA.....B.D.#
    // ###D#.#.#.###
    //   #C#.#C#B#
    //   #########

    // A = 11, B = 3 + 5 + 7*, C = 6, D = 2 + 3 + 8*
    // #############
    // #AA.........#
    // ###.#B#.#D###
    //   #C#B#C#D#
    //   #########

    // A = 11 + 3 + 3*, B = 15, C = 6 + 7*, D = 13
    // #############
    // #...........#
    // ###A#B#C#D###
    //   #A#B#C#D#
    //   #########
    
    return 17 + 150 + 1300 + 13000;
}

